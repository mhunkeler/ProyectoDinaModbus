void Force_Init(void)
{
    HAL_GPIO_PinCfg(DIV_REF_POW,GPIO_OUTPUT);
    HAL_GPIO_PinCfg(OP_CELDA_POW,GPIO_OUTPUT);
    HAL_GPIO_PinCfg(CELDA_POWN,GPIO_OUTPUT);
    HAL_GPIO_PinCfg(CELDA_POWP,GPIO_OUTPUT);
    HAL_GPIO_PinSet(DIV_REF_POW,GPIO_HIGH);
    HAL_GPIO_PinSet(OP_CELDA_POW,GPIO_HIGH);
    HAL_GPIO_PinSet(CELDA_POWN,GPIO_LOW);
    HAL_GPIO_PinSet(CELDA_POWP,GPIO_HIGH);
    HAL_SetAnalog_FORCE();
    analog_init();
}
void Force_ON(void)
{
    HAL_GPIO_PinSet(DIV_REF_POW,GPIO_HIGH);
    HAL_GPIO_PinSet(OP_CELDA_POW,GPIO_HIGH);
    HAL_GPIO_PinSet(CELDA_POWN,GPIO_LOW);
    HAL_GPIO_PinSet(CELDA_POWP,GPIO_HIGH);
}
void Force_OFF(void)
{
    SD1CON1bits.SDON = 0;
    HAL_GPIO_PinSet(DIV_REF_POW,GPIO_LOW);
    HAL_GPIO_PinSet(OP_CELDA_POW,GPIO_LOW);
    HAL_GPIO_PinSet(CELDA_POWP,GPIO_LOW);
}
